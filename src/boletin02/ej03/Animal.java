package boletin02.ej03;

/**
 * Interfaz que contiene los métodos que son comunes a todos los animales
 * 
 * @author Amaro
 */
public interface Animal {
	
	/**
	 * Método que representa la acción de comer
	 */
	void comer();
	
	/**
	 * Método que representa la acción de dormir
	 */
	void dormir();
	
	/**
	 * Método que representa la acción de hacer ruido
	 */
	void hacerRuido();
}
